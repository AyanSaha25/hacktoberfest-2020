    private void beDonorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_beDonorMouseClicked
        
        
       if (validateField()){

      
           try
            {
                Connection conn= MySqlConnect.ConnectDB();
                String sql="INSERT INTO blood_management_system.donar_information(firstName,lastName,phoneNumber,password,age,bloodGroup,anotherContactNumber,gender,location) value (?,?,?,?,?,?,?,?,?)";
                PreparedStatement  pst=conn.prepareStatement(sql);
                pst.setString(1,firstName.getText());
                pst.setString(2,lastName.getText());
                pst.setString(3, phoneNumber.getText());
                pst.setString(4,password.getText());
                pst.setInt(5,Integer.parseInt(age.getText()));
              
                String bloodgroup;
                
                bloodgroup = bloodGroup.getSelectedItem().toString();
                pst.setString(6,bloodgroup);
              
              
                pst.setString(7, contractNumber.getText());
                
                String gender = null;
                
                if(male.isSelected()){
                    
                    gender = male.getText();
                }
                if (female.isSelected()){
                    
                    gender = female.getText();
                }  
                pst.setString(8, gender);
                pst.setString(9, location.getText());
               
                 
                pst.execute();
                 
                 JOptionPane.showMessageDialog(null, "Registration successfull","Success",JOptionPane.PLAIN_MESSAGE);
                 conn.close();
                 dispose();
       
       }
       catch(Exception e)
       {
         
            JOptionPane.showMessageDialog(null, "Registration Failed","ERROR",JOptionPane.PLAIN_MESSAGE);
           
            }
       }
    }//GEN-LAST:event_beDonorMouseClicked

    private void ageKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ageKeyTyped
        
        char c = evt.getKeyChar();
        
        if (!(Character.isDigit(c) || (c==KeyEvent.VK_BACKSPACE) ||(c==KeyEvent.VK_DELETE))){
            
            getToolkit().beep();
            evt.consume();
        }

        
    }//GEN-LAST:event_ageKeyTyped


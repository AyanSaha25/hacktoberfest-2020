    private boolean validateField(){
        
        String pass = password.getText();
        
        if (firstName.getText().isEmpty() || lastName.getText().isEmpty() || phoneNumber.getText().isEmpty() 
                || password.getText().isEmpty() || password2.getText().isEmpty() || contractNumber.getText().isEmpty()
                || age.getText().isEmpty() || location.getText().isEmpty() ||!password2.getText().equals(pass)){
            
            JOptionPane.showMessageDialog(null, "Please Fill up all Requirements Correctly","Warning",JOptionPane.PLAIN_MESSAGE);
            
            return false;
        }
        
        return true;
    }
    
